/* 
 * File:   test_vectors.h
 * 
 * Description: Mock UART payloads for Simulator debugging.
 * Note: 0x00 bytes act as "empty air" to enforce time delays between packets.
 */

#ifndef TEST_VECTORS_H
#define	TEST_VECTORS_H

#include <stdint.h>

// ---------------------------------------------------------
// TEST SUITE 1: Full Write/Read Verification
// ---------------------------------------------------------
const uint8_t TV_STORE_AND_READ[] = {
    // 1. Take control of the bus (Unghost)
    0x5A, 0x08, 0x00, 0x00, 0x00,
    
    // --- 10-byte delay (~100 instruction cycles) ---
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    
    // 2. Write 0xAA to Address 0x1000
    0x5A, 0x02, 0x10, 0x00, 0xAA,
    
    // --- 10-byte delay ---
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,

    // 3. Read from Address 0x1000 (Param byte 0x00 is ignored but required)
    0x5A, 0x01, 0x10, 0x00, 0x00,

    // --- 10-byte delay ---
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,

    // 4. Release the bus (Ghost)
    0x5A, 0x08, 0x00, 0x00, 0x01
};

// ---------------------------------------------------------
// TEST SUITE 2: I/O Port Verification
// ---------------------------------------------------------
const uint8_t TV_IO_TEST[] = {
    0x5A, 0x08, 0x00, 0x00, 0x00,  // Unghost
    0x00, 0x00, 0x00, 0x00, 0x00,
    0x5A, 0x04, 0x00, 0xFF, 0x55,  // OUT 0x55 to Port 0xFF
    0x00, 0x00, 0x00, 0x00, 0x00,
    0x5A, 0x03, 0x00, 0xFF, 0x00,  // IN from Port 0xFF
    0x00, 0x00, 0x00, 0x00, 0x00,
    0x5A, 0x08, 0x00, 0x00, 0x01   // Ghost
};

#endif	/* TEST_VECTORS_H */